CREATE OR REPLACE TABLE Broker_Defaults_Control
        FOR SYSTEM NAME FBROKDFT (
    brkdft_id               FOR COLUMN dfKEY DEC(6, 0)
            NOT NULL GENERATED BY DEFAULT
            AS IDENTITY (START WITH 1 INCREMENT BY 1) ,
    broker_number           FOR COLUMN dfbrok CHAR(10)
            NOT NULL WITH DEFAULT ,
    broker_variable         FOR COLUMN dfvarnam CHAR(30)
            NOT NULL WITH DEFAULT ,
    broker_variable_value   FOR COLUMN dfvarval CHAR(1600)
            NOT NULL WITH DEFAULT ,
    --
    created_ts              FOR COLUMN dfcrttime TIMESTAMP
            NOT NULL WITH DEFAULT CURRENT TIMESTAMP ,
    created_by              FOR COLUMN dfcrtuser CHAR(18)
            NOT NULL WITH DEFAULT USER ,
    updated_ts              FOR COLUMN dfchgtime TIMESTAMP
            NOT NULL FOR EACH ROW ON UPDATE AS ROW change TIMESTAMP ,
    updated_by              FOR COLUMN dfchguser CHAR(18)
            NOT NULL WITH DEFAULT USER)
    RCDFMT RBROKDFT;

-- Add text to the Object so we know the table name
LABEL ON TABLE testdata.Broker_Defaults_Control IS 'Broker Defaults and Control File';

-- Create a trigger to update the Last Update User ID
CREATE OR REPLACE TRIGGER Broker_Defaults_Control_TR01
    BEFORE UPDATE ON Broker_Defaults_Control
    REFERENCING NEW ROW AS NEWROW OLD ROW AS OLDROW
    FOR EACH ROW MODE DB2ROW
    SET NEWROW.updated_by = USER;
