CREATE VARIABLE MOST_RECENT_MONDAY
 FOR SYSTEM NAME MOST_00001
 DATE
 DEFAULT
( CURRENT_DATE + ( 1 - QSYS2 . DAYOFWEEK_ISO ( CURRENT_DATE ) ) DAYS )  ;

GRANT ALTER , READ , WRITE
ON VARIABLE MOST_RECENT_MONDAY
TO BILLB WITH GRANT OPTION ;

GRANT ALTER , READ , WRITE
ON VARIABLE MOST_RECENT_MONDAY
TO PUBLIC ;

;
