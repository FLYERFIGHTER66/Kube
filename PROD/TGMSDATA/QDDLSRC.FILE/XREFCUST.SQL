CREATE OR REPLACE TABLE XREF_FMCUST
FOR SYSTEM NAME XREFCUST (
    xref_id FOR COLUMN XRKEY DEC(6, 0) NOT NULL GENERATED
             BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) ,
    tableCrossReferenced FOR COLUMN XRTABLE CHAR(10)
                NOT NULL WITH DEFAULT ,
    providedByName FOR COLUMN XRPVDNM CHAR(30) NOT NULL WITH DEFAULT ,
    providedByType FOR COLUMN XRPVDTP CHAR(30) NOT NULL WITH DEFAULT ,
    providedByID FOR COLUMN XRPVDID DEC(6,0) NOT NULL WITH DEFAULT ,
    foreignKeyValue FOR COLUMN XRFORKEY CHAR(32) NOT NULL WITH DEFAULT ,
    description FOR COLUMN XRDESC VARCHAR(256) NOT NULL WITH DEFAULT ,
    localKeyValue FOR COLUMN XRLCLKEY CHAR(20) NOT NULL WITH DEFAULT ,
    --
    created_ts FOR COLUMN
    ENCCRTIME TIMESTAMP NOT NULL WITH DEFAULT CURRENT TIMESTAMP ,
    created_by FOR COLUMN ENCCRUSER
    CHAR(18) NOT NULL WITH DEFAULT USER ,
    updated_ts FOR COLUMN ENCUDTIME TIMESTAMP NOT NULL FOR
    EACH ROW ON UPDATE AS ROW change TIMESTAMP ,
    updated_by FOR COLUMN ENCUDUSER CHAR(18) NOT
    NULL WITH DEFAULT USER)
    RCDFMT XRECINVT;

-- Add text to the Object so we know the table name
LABEL ON TABLE XREF_FMCUST IS 'Cross Reference File for FMCUST';

-- Create a trigger to update the Last Update User ID
CREATE OR REPLACE TRIGGER XREF_FMCUST_TR01
    BEFORE UPDATE ON XREF_FMCUST
    REFERENCING NEW ROW AS NEWROW OLD ROW AS OLDROW
    FOR EACH ROW MODE DB2ROW
    SET NEWROW.updated_by = USER;
