  CREATE TABLE FRBBATCH

 ( id              BIGINT         NOT NULL
                                  GENERATED BY DEFAULT AS IDENTITY
                                  (START WITH 1 INCREMENT BY 1 CYCLE)
                                  PRIMARY KEY,
   batchDate       DATE           NOT NULL WITH DEFAULT,
   batchDate7      NUMERIC(7,0)   NOT NULL WITH DEFAULT,
   batchNumber     NUMERIC(3,0)   NOT NULL WITH DEFAULT,
   cart#           NUMERIC(5,0)   NOT NULL WITH DEFAULT,
   tag#            NUMERIC(8,0)   NOT NULL WITH DEFAULT,
   msgCode         CHAR(7)        NOT NULL WITH DEFAULT,
   addedBy         CHAR(10)       NOT NULL WITH DEFAULT,
   addedTs         TIMESTAMP      NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
   changeBy        CHAR(10)       NOT NULL WITH DEFAULT,
   changeTs        TIMESTAMP      NOT NULL
                               FOR EACH ROW ON UPDATE AS ROW CHANGE TIMESTAMP
  ) RCDFMT RRBBATCH
