CREATE OR REPLACE VIEW
TGMSDATA.LOSSBYSIZE

SELECT    LDSIZE
     ,    LDCODE
     ,    LDREDY
     ,    DATE(SUBSTR(CHAR(LDREDY),4,2)||'/'||
               SUBSTR(CHAR(LDREDY),6,2)||'/20'||
               SUBSTR(CHAR(LDREDY),1,2))
     ,    COUNT(*)
FROM      FLOSSD
GROUP BY  LDSIZE, LDCODE, LDREDY
ORDER BY  LDSIZE, LDCODE, LDREDY
