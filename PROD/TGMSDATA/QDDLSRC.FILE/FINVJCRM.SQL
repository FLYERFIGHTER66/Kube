CREATE OR REPLACE VIEW TGMSDATA.FINVJCRM AS


SELECT
FCALEN.QUARTER_4  	AS	QUARTER_4
,FCALEN.NY_QUARTER_4  	AS	NY_QUARTER_4
,FCALEN.DATEFLD  	AS	TRANSACTION_DATE
,FMTAXF.Jurisdiction  	AS	JURISDICTION
,FMTAXF.TXJUNM  	AS	JUR_NAME
,FMTAXF.TXPCT1  	AS	TAX_PERCENT
,'INV'  	        AS	TRANSACTION_TYPE
,FHORDW.WHINV#  	AS	INVOICE_NUMBER
,0	                AS	CREDIT_MEMO_NUMBER
,FHORDW.WHCUST  	AS	CUSTOMER_NUMBER
,FHORDW.WHNAME  	AS	CUSTOMER_NAME
,FHORDW.WHSHP2  	AS	ADDRESS_1
,FHORDW.WHSHP3  	AS	ADDRESS_2
,FHORDW.WHSHCT  	AS	CITY
,FHORDW.WHSHST  	AS	STATE
,FHORDW.WHSHZP  	AS	ZIP_CODE
,FHORDW.WHTXST          AS      TAX_EXEMPT
,FHORDW.WHMER$  	AS	MER_AMOUNT
,FHORDW.WHFRT$  	AS	FRT_AMOUNT
,FHORDW.WHDIS$  	AS	DIS_AMOUNT
,FHORDW.WHMIS$  	AS	MIS_AMOUNT
,FHORDW.WHMER$
    +FHORDW.WHFRT$
    +FHORDW.WHDIS$
    +FHORDW.WHMIS$      AS	GROSS_SALES
,FHORDW.WHTAX$  	AS	TAX_AMOUNT
,FHORDW.WH$AMT  	AS	TOT_AMOUNT
,' '	                AS	COMMENT


FROM    S105R9YM.TGMSDATA.FHORDW FHORDW
,       S105R9YM.TGMSDATA.FMTAXJUL01 FMTAXF
,       S105R9YM.TGMSDATA.FCALEN_ALL FCALEN

WHERE   FHORDW.WHTXJU = FMTAXF.TXTXJU
AND     FHORDW.WHINDT = FCALEN.MDY6



UNION



SELECT
FCALEN.QUARTER_4  	AS	QUARTER_4
,FCALEN.NY_QUARTER_4  	AS	NY_QUARTER_4
,FCALEN.DATEFLD  	AS	TRANSACTION_DATE
,FMTAXF.Jurisdiction  	AS	JURISDICTION
,FMTAXF.TXJUNM  	AS	JUR_NAME
,FMTAXF.TXPCT1  	AS	TAX_PERCENT
,'C/M'  	        AS	TRANSACTION_TYPE
,FHORCW.CHAPIN	        AS	INVOICE_NUMBER
,FHORCW.CHCRM#  	AS	CREDIT_MEMO_NUMBER
,FHORCW.CHCUST 	        AS	CUSTOMER_NUMBER
,FHORCW.CHNAME  	AS	CUSTOMER_NAME
,FHORCW.CHSHP2  	AS	ADDRESS_1
,FHORCW.CHSHP3  	AS	ADDRESS_2
,FHORCW.CHSHCT  	AS	CITY
,FHORCW.CHSHST  	AS	STATE
,FHORCW.CHSHZP  	AS	ZIP_CODE
,FHORCW.CHTXST          AS      TAX_EXEMPT
,FHORCW.CHMER$  	AS	MER_AMOUNT
,FHORCW.CHFRT$  	AS	FRT_AMOUNT
,FHORCW.CHDIS$  	AS	DIS_AMOUNT
,FHORCW.CHMIS$  	AS	MIS_AMOUNT
,FHORCW.CHMER$
    +FHORCW.CHFRT$
    +FHORCW.CHDIS$
    +FHORCW.CHMIS$      AS	GROSS_SALES
,FHORCW.CHTAX$  	AS	TAX_AMOUNT
,FHORCW.CHTOT$  	AS	TOT_AMOUNT
,FHORCW.CHCOMM  	AS	COMMENT


FROM    S105R9YM.TGMSDATA.FHORCW FHORCW
,       S105R9YM.TGMSDATA.FMTAXJUL01 FMTAXF
,       S105R9YM.TGMSDATA.FCALEN_ALL FCALEN

WHERE   FHORCW.CHTXJU = FMTAXF.TXTXJU
AND     FHORCW.CHTDAT = FCALEN.MDY6
