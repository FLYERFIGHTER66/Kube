    CREATE OR REPLACE VIEW
    TGMSDATA.SIZE_CODE_VIEW AS

    SELECT
    FMSIZC.SCCATG,
    FMSIZD.SZCATG,
    FMSIZE.SZCODE,
    FMSIZE.SZSTAT,
    FMSIZE.SZDESC,
    FMSIZD.SZCTRD,
    FMSIZD.SZLONG,
    FMSIZE.SZCUBF,
    FMSIZE.SZPERC,
    FMSIZE.SZCELL,
    FMSIZE.SZSALE,
    FMSIZE.SZTAGS,
    FMSIZE.SZPSHF,
    FMSIZE.SZSHLV,
    FMSIZE.SZPCRT,
    FMSIZD.SZCTNR,
    FMSIZD.SZROWS

    FROM
    S105R9YM.TGMSDATA.FMSIZC FMSIZC, S105R9YM.TGMSDATA.FMSIZD FMSIZD, S105R9YM.TGMSDATA.FMSIZE
    FMSIZE

    WHERE
    FMSIZD.SZCODE = FMSIZE.SZCODE
    AND FMSIZC.SCSIZE = FMSIZD.SZCODE
