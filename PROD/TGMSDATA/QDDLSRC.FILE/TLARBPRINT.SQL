CREATE OR REPLACE VIEW TLA_READY_BAY_PRINTERS_LIST AS
WITH PRINTERS_ARRAY ( NUMBER, PRINTER_LIST ) AS
(  VALUES (1, TRIM(TLA_READY_BAY_OUTQ)||'
')
UNION
   SELECT NUMBER + 1, TRIM(PRINTER_LIST)||','||TRIM(TLA_READY_BAY_OUTQ)
   FROM PRINTERS_ARRAY
   WHERE NUMBER < 15
)

SELECT * FROM PRINTERS_ARRAY
