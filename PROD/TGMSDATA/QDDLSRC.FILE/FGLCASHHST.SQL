CREATE OR REPLACE TABLE GL_Cash_Position_History_File
        FOR SYSTEM NAME FGLCASHHST (

    GL_Year             FOR COLUMN CHYEAR DEC(4,0)      NOT NULL WITH DEFAULT ,
    GL_Week             FOR COLUMN CHWEEK DEC(2,0)      NOT NULL WITH DEFAULT ,
    GL_WeekDay          FOR COLUMN CHWKDY DEC(2,0)      NOT NULL WITH DEFAULT ,
    Transaction_Date    FOR COLUMN CHDATE DATE          NOT NULL WITH DEFAULT ,
    GL_Number           FOR COLUMN CHGLNO DEC(8, 0)     NOT NULL WITH DEFAULT ,
    Description         FOR COLUMN CHDESC CHAR(30)      NOT NULL WITH DEFAULT ,
    Transaction_Amount  FOR COLUMN CH$AMT DEC(11, 2)    NOT NULL WITH DEFAULT ,
    --
    created_ts          FOR COLUMN aucrttime TIMESTAMP  NOT NULL WITH DEFAULT CURRENT TIMESTAMP
    )  RCDFMT RGLCASHHST;
