CREATE OR REPLACE VARIABLE NEXT_SOW_DATE_CUT DATE DEFAULT
  (CASE  WHEN DAYOFWEEK_ISO(CURRENT DATE) <= 2
      THEN ((CURRENT DATE) - ((DAYOFWEEK_ISO(CURRENT DATE)-1)-0) DAYS) + 7 days
    WHEN DAYOFWEEK_ISO(CURRENT DATE) > 2
      THEN ((CURRENT DATE) - ((DAYOFWEEK_ISO(CURRENT DATE)-1)-7) DAYS) + 7 days
  END);
